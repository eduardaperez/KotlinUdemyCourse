/**
 * variáveis armazenam valores
 * entrada, processamento e saída - variáveis ajudam nisso
 * eles podem ser escolhidos semanticamente com o assunto
 * sua declaração inicia com - var ou val
 * não podem assumir palavras reservadas
 * var = mutáveis
 * val = imutáveis
 * toda variável tem tipo - linguagem tipada
 */

fun main() {
    var nome = "Camila"
    nome = "Eduarda"

    val sobrenome = "Perez"
//  sobrenome = "z" xx

    println(nome)
}
